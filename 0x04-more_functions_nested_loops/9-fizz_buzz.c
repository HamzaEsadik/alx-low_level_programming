#include<stdio.h>

/**
 * main - entry point
 * print numbers from 0 to 100
 * instead of multi 3 and 5
 * Return: null
 */
int main(void)
{
	int i;

	for (i = 1; i <= 100; i++)
	{
		if (i % 3 == 0 && i % 5 != 0)
		{
			printf(" Fizz");
		} else if (i % 5 == 0 && i % 3 != 0)
		{
			printf(" Buzz");
		} else if (i % 3 == 0 && i % 5 == 0)
		{
			printf(" FizzBuzz");
		} else if (i == 1)
		{
			printf("%d", i);
		} else
		{
			printf("%d ", i);
		}
	}
	printf("\n");
	return (0);
}
